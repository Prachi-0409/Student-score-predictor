import streamlit as st
import joblib
import numpy as np

rf_model = joblib.load("random_forest_model.pkl")
lr_model = joblib.load("linear_regression_model.pkl")

st.title("Student Exam Score Predictor")

hours = st.slider("Weekly Self-Study Hours", 0, 60, 10)
attendance = st.slider("Attendance (%)", 0, 100, 75)
activity = st.radio("Extracurricular Activities", ["Yes", "No"])
activity_val = 1 if activity == "Yes" else 0

input_features = np.array([[hours, attendance, activity_val]])

if st.button("Predict Score"):
    rf_pred = rf_model.predict(input_features)[0]
    lr_pred = lr_model.predict(input_features)[0]

    st.success(f"Random Forest Predicted Score: **{rf_pred:.2f}**")
    st.info(f"Linear Regression Predicted Score: **{lr_pred:.2f}**")

    st.markdown("---")
    st.write("**Random Forest** is usually more accurate, but Linear Regression is easier to explain.")

